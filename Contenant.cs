        public Contenant(int maxWeight)
        {
            _maxWeight = maxWeight;
            _currentWeight = 0;
        }
        public void AddGemToChest(Gems gem)
        {
            if (CurrentWeight + gem.Weight < MaxWeight)
            {
                gemList.Add(gem);
                CurrentWeight += gem.Weight;
                Console.WriteLine($"A {gem.Type} has been added to the chest.");
            }
            else
            {
                Console.WriteLine("The maximum capacity is exceeded.");
            }
        }
        public void RemoveGemFromChest(Gems gem)
        {
            gemList.Remove(gem);
        }
        public static int[] GemTotal()  // 0 = Value, 1 = Weight
        {
            int[] total = new int[2];
            foreach (Gems gem in gemList)
            {
                total[0] += (int)gem.Value;
                total[1] += gem.Weight;
            }
            return total;
        }
